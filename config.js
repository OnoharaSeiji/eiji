window.getFaseById = function(id) {
  return (window.CONFIG?.fases || []).find(f => f.id === id) || null;
};

window.getSemana = function(numero) {
  return (window.CONFIG?.semanas || []).find(s => s.semana === numero) || null;
};

window.getDia = function(numero) {
  return (window.CONFIG?.dias || []).find(d => d.numero === numero) || null;
};

window.getDiaByDate = function(dateISO) {
  return (window.CONFIG?.dias || []).find(d => d.data === dateISO) || null;
};

window.getRecompensaDaSemana = function(semana) {
  const week = (window.CONFIG.semanas || []).find(w => w.semana === semana);
  return week ? week.recompensaSemana : null;
};
